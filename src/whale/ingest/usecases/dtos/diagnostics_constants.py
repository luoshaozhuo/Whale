    REDIS_MEMORY_FULL = "REDIS_MEMORY_FULL"

    # Kafka
    KAFKA_BROKER_UNAVAILABLE = "KAFKA_BROKER_UNAVAILABLE"
    KAFKA_ACK_TIMEOUT = "KAFKA_ACK_TIMEOUT"

    # Snapshot
    SNAPSHOT_EMPTY = "SNAPSHOT_EMPTY"
    SNAPSHOT_ALL_STALE = "SNAPSHOT_ALL_STALE"

    # Config
    CONFIG_PROTOCOL_ADAPTER_MISSING = "CONFIG_PROTOCOL_ADAPTER_MISSING"
    CONFIG_ENDPOINT_MISSING = "CONFIG_ENDPOINT_MISSING"
    CONFIG_TIMEOUT_INVALID = "CONFIG_TIMEOUT_INVALID"
    CONFIG_PROFILE_ITEMS_EMPTY = "CONFIG_PROFILE_ITEMS_EMPTY"
