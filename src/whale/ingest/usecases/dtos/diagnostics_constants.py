    """失败类别，用于 ingest_source_health 和 ingest_runtime_event."""

    CONFIG_ERROR = "CONFIG_ERROR"
    SOURCE_UNAVAILABLE = "SOURCE_UNAVAILABLE"
    SOURCE_TIMEOUT = "SOURCE_TIMEOUT"
    SOURCE_PARTIAL_FAILURE = "SOURCE_PARTIAL_FAILURE"
    DATA_INVALID = "DATA_INVALID"
    CACHE_UPDATE_FAILED = "CACHE_UPDATE_FAILED"
    SNAPSHOT_READ_FAILED = "SNAPSHOT_READ_FAILED"
    SNAPSHOT_EMPTY = "SNAPSHOT_EMPTY"
    SNAPSHOT_STALE = "SNAPSHOT_STALE"
    SNAPSHOT_INVALID = "SNAPSHOT_INVALID"
    PUBLISH_FAILED = "PUBLISH_FAILED"
    CYCLE_OVERRUN = "CYCLE_OVERRUN"
    JOB_EXECUTION_FAILED = "JOB_EXECUTION_FAILED"
    RESOURCE_EXHAUSTED = "RESOURCE_EXHAUSTED"
    UNKNOWN_ERROR = "UNKNOWN_ERROR"


