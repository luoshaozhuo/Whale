    OPCUA_READ_TIMEOUT = "OPCUA_READ_TIMEOUT"
    OPCUA_BAD_STATUS_CODE = "OPCUA_BAD_STATUS_CODE"
    OPCUA_PARTIAL_READ_FAILED = "OPCUA_PARTIAL_READ_FAILED"

